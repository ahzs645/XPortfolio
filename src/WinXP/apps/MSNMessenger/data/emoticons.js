export function parseEmoticons(text) {
  let result = text;
  Object.entries(emoticonMap).forEach(([shortcut, data]) => {
    result = result.split(shortcut).join(
      `<img src="${ASSET_PATH}emoticons/${data.file}" class="emoticon" title="${shortcut}" alt="${shortcut}">`
    );
  });
  return result;
}
